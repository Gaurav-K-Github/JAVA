package GIT_projects.src;

public class Conditionals {
    public static void main(String[] args){
        int i1=2;
        if (i1!=0){//Simple if condition
            System.out.println("Non zero number!");
        };

        int i2=0;
        if (i2!=0){//If-Else statement
            System.out.println("Non zero number!");
        }
        else{
            System.out.println("Zero!");
        };




    }
}
