//7. Write a program to check whether a number is even or odd.

public class Q7{
    public static void main(String[] args) {
        int n1=0;

        if(n1==0){
            System.out.println("Zero found!");
        }else if (n1%2==0){
            System.out.println("Even");
        }else{
            System.out.println("Odd");
        }
    }
}
